package com.zikr.app.data

import com.zikr.app.model.DhikrPeriod
import com.zikr.app.model.Surah

object QuranRepository {

    private val commonSurahs = listOf(
        Surah(
            id = "fatiha",
            title = "الفاتحة",
            text = """
                بسم الله الرحمن الرحيم
                الحمد لله رب العالمين
                الرحمن الرحيم
                مالك يوم الدين
                إياك نعبد وإياك نستعين
                اهدنا الصراط المستقيم
                صراط الذين أنعمت عليهم غير المغضوب عليهم ولا الضالين
            """.trimIndent()
        ),
        Surah(
            id = "ikhlas",
            title = "الإخلاص",
            text = """
                بسم الله الرحمن الرحيم
                قل هو الله أحد
                الله الصمد
                لم يلد ولم يولد
                ولم يكن له كفوا أحد
            """.trimIndent()
        ),
        Surah(
            id = "falaq",
            title = "الفلق",
            text = """
                بسم الله الرحمن الرحيم
                قل أعوذ برب الفلق
                من شر ما خلق
                ومن شر غاسق إذا وقب
                ومن شر النفاثات في العقد
                ومن شر حاسد إذا حسد
            """.trimIndent()
        ),
        Surah(
            id = "nas",
            title = "الناس",
            text = """
                بسم الله الرحمن الرحيم
                قل أعوذ برب الناس
                ملك الناس
                إله الناس
                من شر الوسواس الخناس
                الذي يوسوس في صدور الناس
                من الجنة والناس
            """.trimIndent()
        ),
        Surah(
            id = "ayat_al_kursi",
            title = "آية الكرسي",
            text = """
                الله لا إله إلا هو الحي القيوم
                لا تأخذه سنة ولا نوم
                له ما في السماوات وما في الأرض
                من ذا الذي يشفع عنده إلا بإذنه
                يعلم ما بين أيديهم وما خلفهم
                ولا يحيطون بشيء من علمه إلا بما شاء
                وسع كرسيه السماوات والأرض
                ولا يؤوده حفظهما وهو العلي العظيم
            """.trimIndent()
        ),
        Surah(
            id = "quraysh",
            title = "قريش",
            text = """
                بسم الله الرحمن الرحيم
                لإيلاف قريش
                إيلافهم رحلة الشتاء والصيف
                فليعبدوا رب هذا البيت
                الذي أطعمهم من جوع وآمنهم من خوف
            """.trimIndent()
        )
    )

    private val surahAshShams = Surah(
        id = "ash_shams",
        title = "الشمس",
        text = """
            بسم الله الرحمن الرحيم
            والشمس وضحاها
            والقمر إذا تلاها
            والنهار إذا جلاها
            والليل إذا يغشاها
            والسماء وما بناها
            والأرض وما طحاها
            ونفس وما سواها
            فألهمها فجورها وتقواها
            قد أفلح من زكاها
            وقد خاب من دساها
            كذبت ثمود بطغواها
            إذ انبعث أشقاها
            فقال لهم رسول الله ناقة الله وسقياها
            فكذبوه فعقروها فدمدم عليهم ربهم بذنبهم فسواها
            ولا يخاف عقباها
        """.trimIndent()
    )

    private val surahAlLayl = Surah(
        id = "al_layl",
        title = "الليل",
        text = """
            بسم الله الرحمن الرحيم
            والليل إذا يغشى
            والنهار إذا تجلى
            وما خلق الذكر والأنثى
            إن سعيكم لشتى
            فأما من أعطى واتقى
            وصدق بالحسنى
            فسنيسره لليسرى
            وأما من بخل واستغنى
            وكذب بالحسنى
            فسنيسره للعسرى
            وما يغني عنه ماله إذا تردى
            إن علينا للهدى
            وإن لنا للآخرة والأولى
            فأنذرتكم نارا تلظى
            لا يصلاها إلا الأشقى
            الذي كذب وتولى
            وسيجنبها الأتقى
            الذي يؤتي ماله يتزكى
            وما لأحد عنده من نعمة تجزى
            إلا ابتغاء وجه ربه الأعلى
            ولسوف يرضى
        """.trimIndent()
    )

    fun surahsFor(period: DhikrPeriod): List<Surah> {
        return when (period) {
            DhikrPeriod.MORNING -> commonSurahs + surahAshShams
            DhikrPeriod.EVENING -> commonSurahs + surahAlLayl
        }
    }
}
